def render_4_cards(mdex, deck):
    """
    Renders Four Cards on the same Horizontal plane.
    """
    card_one_arr = deck[mdex].splitlines()
    card_two_arr = deck[mdex+1].splitlines()
    card_three_arr = deck[mdex+2].splitlines()
    card_four_arr = deck[mdex+3].splitlines()

    for each in range(len(card_one_arr)):
        if each == len(card_one_arr)-1 or each == 1:
            print(card_one_arr[each]+"     "+card_two_arr[each]+"     "+card_three_arr[each]+"     "+card_four_arr[each])
        else:
            print(card_one_arr[each]+"    "+card_two_arr[each]+"    "+card_three_arr[each]+"    "+card_four_arr[each]) 
    return

